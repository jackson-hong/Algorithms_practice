import java.util.Scanner;

public class Main {
    static Scanner sc = new Scanner(System.in);
    public static void main(String[] args) {
        System.out.println(asciiCode(sc.next().charAt(0)));
    }
    // baekjoon #11654
    static int asciiCode(char num){
        int result = 0;

        result = num;

        return result;
    }
}
